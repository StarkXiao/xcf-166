export function getTasksBySeasonId(seasonId: string): SeasonTask[] {
  return seasonTasks.filter((t) => t.seasonId === seasonId)
}

export function getTasksByType(seasonId: string, type: string): SeasonTask[] {
  return seasonTasks
    .filter((t) => t.seasonId === seasonId && t.type === type)
    .sort((a, b) => a.order - b.order)
}
