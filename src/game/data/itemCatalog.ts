const itemMap = new Map<string, GameItemDef>()
for (const item of dungeonDropItems) {
  if (!itemMap.has(item.id)) {
    itemMap.set(item.id, item)
  }
}

export function getItemDef(itemId: string): GameItemDef | undefined {
  return itemMap.get(itemId)
}

export function getAllDungeonDropDefs(): GameItemDef[] {
  return Array.from(itemMap.values())
}

export function registerItemDef(item: GameItemDef) {
  if (!itemMap.has(item.id)) {
    itemMap.set(item.id, item)
  }
}

export function isDungeonDrop(itemId: string): boolean {
  return itemMap.has(itemId)
}
